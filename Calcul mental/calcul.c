int addition(int nombrea, int nombreb)
{
	return nombrea + nombreb;
}
int multiplication(float nombrea, float nombreb)
{
	return nombrea * nombreb;
}
float division(float nombrea, float nombreb)
{
	return nombrea/nombreb;
}
int soustraction(float nombrea, float nombreb)
{
	return nombrea - nombreb;
}